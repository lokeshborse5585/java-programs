class Double_  //8 byte 
{
	public static void main(String[] args) 
	{// done by the  explicit 
		double a=67.123;
		System.out.println("double  value "+a);
	 //double to byte
		 byte b=(byte)a;
		
		System.out.println("double to  byte  "+b);
		//double to short
		 short c=(short)a;
		
		System.out.println("double to short   "+c);
		//double to char
		char i =(char)a;
		
		System.out.println("double to char  "+i);
		
		//double to int
		 int d=(int)a;
		
		System.out.println("double to int "+d);
		//double to float
		 float e=(float)a;
		
		System.out.println("double to Float  "+e);
	//	System.out.println("double to  long   "+e);
		long u=(long)a;
		
		System.out.println("double to long  "+u);
		
		
	}
}
